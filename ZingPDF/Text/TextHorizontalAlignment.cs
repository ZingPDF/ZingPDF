namespace ZingPDF.Text;

/// <summary>
/// Horizontal alignment within a page text layout box.
/// </summary>
public enum TextHorizontalAlignment
{
    /// <summary>
    /// Align to the start edge of the reading direction.
    /// </summary>
    Start,

    /// <summary>
    /// Center within the layout bounds.
    /// </summary>
    Center,

    /// <summary>
    /// Align to the end edge of the reading direction.
    /// </summary>
    End
}
